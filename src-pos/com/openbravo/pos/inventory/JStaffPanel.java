    private void jbtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDeleteActionPerformed

        int index = m_jStaffList.getSelectedIndex();
        System.out.println("index is " + index);

        if (index == -1) {
            JOptionPane.showMessageDialog(JStaffPanel.this, "Please select the Name");
        } else {
            try {
                String val = m_jStaffList.getSelectedValue().toString();
                dlCustomers.deleteStaff(val);
                 model.remove(index);
                 searchKeyModel.remove(index);
                 list.remove(index);
                 m_jSearchKey.setText("");
                 m_jName.setText("");
                 m_jPhNo.setText("");
                 jCheckActive.setSelected(false);
           
            } catch (BasicException ex) {
                Logger.getLogger(JStaffPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                populateList();
            } catch (BasicException ex) {
                Logger.getLogger(JStaffPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
            updateMode = false;
        }
}//GEN-LAST:event_jbtnDeleteActionPerformed

