    public void populateList() throws BasicException {

        model = new DefaultListModel();
        m_jStaffList.setModel(model);
        searchKeyModel= new DefaultListModel();
        list = dlCustomers.getStaffList();
        for (int i = 0; i < list.size(); i++) {
             String listid = list.get(i).getName();
             String searchKey=list.get(i).getSearchkey();
             model.add(i, listid);
             searchKeyModel.add(i, searchKey);
        }
    }

private void saveButtonAction() throws BasicException {

        String skey = m_jSearchKey.getText();
        String name = m_jName.getText();
         String phoneNo = m_jPhNo.getText();
         String active="";
         if(jCheckActive.isSelected())
             active="Y";
         else
             active="N";    
         try {
                list = dlCustomers.getStaffList();
           System.out.println("updatemode"+updateMode);
                if (updateMode == false) {
                    if (searchKeyModel.contains(skey)) {
                         JOptionPane.showMessageDialog(this, "Entered Search Key already exists");
                     } else {
                        dlCustomers.insertStaffDetails(RandomUtil.genRandom32Hex(),skey,name,phoneNo,active);
                    }
                } else {
                    String id = list.get(m_jStaffList.getSelectedIndex()).getId();
                    dlCustomers.updateStaffDetails(id,skey,name,phoneNo,active);
                    updateMode = false;
                }
                populateList();
                m_jSearchKey.setText("");
                m_jName.setText("");
                m_jPhNo.setText("");
                jCheckActive.setSelected(false);
             } catch (Exception npe) {
              //   npe.printStackTrace();
                JOptionPane.showMessageDialog(this, "Enter valid Search Key");
                m_jSearchKey.setText("");
                m_jName.setText("");
                m_jPhNo.setText("");
                jCheckActive.setSelected(false);
             }
       
    }


     public static String genRandom32Hex() {
    SecureRandom ng = numberGenerator;
    if (ng == null) {
      numberGenerator = ng = new SecureRandom();
    }

    return Long.toHexString(MSB | ng.nextLong()) + Long.toHexString(MSB | ng.nextLong());
  }
